package JAVA.src.JAVABasics;
public class Comments
{
    public static void main(String[] args) {
        System.out.println("//This is single line comment");
        //This is single line comment using '//' line
        System.out.println("/*This is multi-line comment*/");
        /*This is multi-line comment using start with '/*' and end with */
        System.out.println("/** This is a Documentation comment */");
    }
}